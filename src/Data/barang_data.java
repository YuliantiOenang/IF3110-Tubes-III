package Data;

public class barang_data {
	String id_barang;
	String nama_barang;
	String gambar_barang;
	String harga_barang;
	String kategori_barang;
	String n_beli;
	String keterangan;
	String stok;
	public String getId_barang() {
		return id_barang;
	}
	public void setId_barang(String id_barang) {
		this.id_barang = id_barang;
	}
	public String getNama_barang() {
		return nama_barang;
	}
	public void setNama_barang(String nama_barang) {
		this.nama_barang = nama_barang;
	}
	public String getGambar_barang() {
		return gambar_barang;
	}
	public void setGambar_barang(String gambar_barang) {
		this.gambar_barang = gambar_barang;
	}
	public String getHarga_barang() {
		return harga_barang;
	}
	public void setHarga_barang(String harga_barang) {
		this.harga_barang = harga_barang;
	}
	public String getKategori_barang() {
		return kategori_barang;
	}
	public void setKategori_barang(String kategori_barang) {
		this.kategori_barang = kategori_barang;
	}
	public String getN_beli() {
		return n_beli;
	}
	public void setN_beli(String n_beli) {
		this.n_beli = n_beli;
	}
	public String getKeterangan() {
		return keterangan;
	}
	public void setKeterangan(String keterangan) {
		this.keterangan = keterangan;
	}
	public String getStok() {
		return stok;
	}
	public void setStok(String stok) {
		this.stok = stok;
	}
	
}
