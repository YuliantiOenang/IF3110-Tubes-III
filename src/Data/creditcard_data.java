package Data;

public class creditcard_data {
	String card_id;
	String card_nameon;
	String card_expdate;
	String card_owner;
	public String getCard_id() {
		return card_id;
	}
	public void setCard_id(String card_id) {
		this.card_id = card_id;
	}
	public String getCard_nameon() {
		return card_nameon;
	}
	public void setCard_nameon(String card_nameon) {
		this.card_nameon = card_nameon;
	}
	public String getCard_expdate() {
		return card_expdate;
	}
	public void setCard_expdate(String card_expdate) {
		this.card_expdate = card_expdate;
	}
	public String getCard_owner() {
		return card_owner;
	}
	public void setCard_owner(String card_owner) {
		this.card_owner = card_owner;
	}
	
	
	
}
